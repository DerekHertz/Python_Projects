def translate_input(user_input):

    new_string = ''

    for letter in user_input:
        if letter == " ":
            new_string += " "
        else:
            new_string += morse_code.get(letter, "")+ " "

    print(new_string.strip())


user_input = input("What word or sentence would you like to translate?" ).upper()
translate_input(user_input)
